use macroquad::prelude::*;

pub struct SpriteManager {
    pub bub_idle: Texture2D,
    pub bub_walk: [Texture2D; 2],
    pub bub_blow: Texture2D,
    pub bob_idle: Texture2D,
    pub bob_walk: [Texture2D; 2],
    pub bob_blow: Texture2D,
    pub zen_chan: [Texture2D; 2],
    pub bubble: Texture2D,
    pub apple: Texture2D,
    pub tile: Texture2D,
}

impl SpriteManager {
    pub async fn new() -> Self {
        let palette = [
            Color::from_rgba(0, 0, 0, 0),         // 0: Transparent
            Color::from_rgba(255, 255, 255, 255), // 1: White
            Color::from_rgba(0, 0, 0, 255),       // 2: Black
            Color::from_rgba(34, 177, 76, 255),   // 3: Dark Green
            Color::from_rgba(181, 230, 29, 255),  // 4: Light Green
            Color::from_rgba(0, 162, 232, 255),   // 5: Dark Blue
            Color::from_rgba(153, 217, 234, 255), // 6: Light Blue
            Color::from_rgba(237, 28, 36, 255),   // 7: Red
            Color::from_rgba(255, 127, 39, 255),  // 8: Orange
            Color::from_rgba(255, 242, 0, 255),   // 9: Yellow
            Color::from_rgba(63, 72, 204, 255),   // 10: Purpleish Blue
            Color::from_rgba(127, 127, 127, 255), // 11: Gray
            Color::from_rgba(195, 195, 195, 255), // 12: Light Gray
            Color::from_rgba(136, 0, 21, 255),    // 13: Maroon
            Color::from_rgba(255, 174, 201, 255), // 14: Pink
            Color::from_rgba(77, 109, 243, 180),  // 15: Bubble Blue
        ];

        Self {
            bub_idle: create_texture(palette, &BUB_IDLE),
            bub_walk: [
                create_texture(palette, &BUB_WALK_1),
                create_texture(palette, &BUB_WALK_2),
            ],
            bub_blow: create_texture(palette, &BUB_BLOW),
            bob_idle: create_texture(palette, &BOB_IDLE),
            bob_walk: [
                create_texture(palette, &BOB_WALK_1),
                create_texture(palette, &BOB_WALK_2),
            ],
            bob_blow: create_texture(palette, &BOB_BLOW),
            zen_chan: [
                create_texture(palette, &ZEN_WALK_1),
                create_texture(palette, &ZEN_WALK_2),
            ],
            bubble: create_texture(palette, &BUBBLE_PIXELS),
            apple: create_texture(palette, &APPLE_PIXELS),
            tile: create_texture(palette, &TILE_PIXELS),
        }
    }
}

fn create_texture(palette: [Color; 16], pixels: &[u8]) -> Texture2D {
    let mut data = Vec::with_capacity(16 * 16 * 4);
    for &p in pixels {
        let color = palette[p as usize];
        data.push((color.r * 255.0) as u8);
        data.push((color.g * 255.0) as u8);
        data.push((color.b * 255.0) as u8);
        data.push((color.a * 255.0) as u8);
    }
    let texture = Texture2D::from_rgba8(16, 16, &data);
    texture.set_filter(FilterMode::Nearest);
    texture
}

