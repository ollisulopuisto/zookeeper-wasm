use macroquad::prelude::*;

pub struct SpriteManager {
    pub bub: Texture2D,
    pub bob: Texture2D,
    pub zen_chan: Texture2D,
    pub bubble: Texture2D,
    pub apple: Texture2D,
    pub banana: Texture2D,
    pub tile: Texture2D,
}

impl SpriteManager {
    pub async fn new() -> Self {
        let palette = [
            Color::from_rgba(0, 0, 0, 0),       // 0: Transparent
            WHITE,                             // 1: White
            BLACK,                             // 2: Black
            Color::from_rgba(0, 255, 0, 255),   // 3: Bub Green
            Color::from_rgba(0, 100, 255, 255), // 4: Bob Blue
            Color::from_rgba(255, 255, 0, 255), // 5: Zen-Chan Yellow
            RED,                               // 6: Red
            Color::from_rgba(255, 100, 200, 255),// 7: Pink
            Color::from_rgba(100, 200, 255, 150),// 8: Bubble Light Blue
        ];

        Self {
            bub: create_texture(palette, &BUB_PIXELS),
            bob: create_texture(palette, &BOB_PIXELS),
            zen_chan: create_texture(palette, &ZEN_CHAN_PIXELS),
            bubble: create_texture(palette, &BUBBLE_PIXELS),
            apple: create_texture(palette, &APPLE_PIXELS),
            banana: create_texture(palette, &BANANA_PIXELS),
            tile: create_texture(palette, &TILE_PIXELS),
        }
    }
}

fn create_texture(palette: [Color; 9], pixels: &[u8]) -> Texture2D {
    let mut data = Vec::with_capacity(16 * 16 * 4);
    for &p in pixels {
        let color = palette[p as usize];
        data.push((color.r * 255.0) as u8);
        data.push((color.g * 255.0) as u8);
        data.push((color.b * 255.0) as u8);
        data.push((color.a * 255.0) as u8);
    }
    let texture = Texture2D::from_rgba8(16, 16, &data);
    texture.set_filter(FilterMode::Nearest);
    texture
}

